drop function if exists public.dkd_web_courier_operation_snapshot_dkd();
create function public.dkd_web_courier_operation_snapshot_dkd()
returns jsonb
language plpgsql
security definer
set search_path = public, auth
as $dkd_snapshot$
declare
  dkd_user_id_value uuid := auth.uid();
  dkd_has_courier_license_value boolean := false;
  dkd_profile_json_value jsonb := '{}'::jsonb;
  dkd_jobs_json_value jsonb := '[]'::jsonb;
  dkd_shipments_json_value jsonb := '[]'::jsonb;
begin
  if dkd_user_id_value is null then
    raise exception 'auth_required';
  end if;

  insert into public.dkd_profiles (user_id)
  values (dkd_user_id_value)
  on conflict (user_id) do nothing;

  dkd_has_courier_license_value := public.dkd_web_courier_license_active_dkd(dkd_user_id_value);

  select to_jsonb(dkd_profile_row) || jsonb_build_object('has_courier_license', dkd_has_courier_license_value)
  into dkd_profile_json_value
  from public.dkd_profiles dkd_profile_row
  where dkd_profile_row.user_id = dkd_user_id_value;

  if dkd_has_courier_license_value is true then
    select coalesce(jsonb_agg(to_jsonb(dkd_job_row) order by dkd_job_row.created_at desc), '[]'::jsonb)
    into dkd_jobs_json_value
    from public.dkd_courier_jobs dkd_job_row
    where coalesce(dkd_job_row.is_active, true) is true
      and (
        dkd_job_row.assigned_user_id = dkd_user_id_value
        or (
          dkd_job_row.assigned_user_id is null
          and coalesce(dkd_job_row.status, 'open') in ('open', 'ready', 'published')
        )
      );
  else
    dkd_jobs_json_value := '[]'::jsonb;
  end if;

  select coalesce(jsonb_agg(
    jsonb_build_object(
      'id', dkd_shipment_row.id,
      'customer_user_id', dkd_shipment_row.customer_user_id,
      'customer_first_name', dkd_shipment_row.customer_first_name,
      'customer_last_name', dkd_shipment_row.customer_last_name,
      'customer_full_name', trim(concat(dkd_shipment_row.customer_first_name, ' ', dkd_shipment_row.customer_last_name)),
      'customer_phone_text', dkd_shipment_row.customer_phone_text,
      'pickup_address_text', dkd_shipment_row.pickup_address_text,
      'delivery_address_text', dkd_shipment_row.delivery_address_text,
      'delivery_note', dkd_shipment_row.delivery_note,
      'package_content_text', dkd_shipment_row.package_content_text,
      'package_image_url', dkd_shipment_row.package_image_url,
      'package_weight_kg', dkd_shipment_row.package_weight_kg,
      'status', dkd_shipment_row.status,
      'courier_job_id', dkd_shipment_row.courier_job_id,
      'courier_fee_tl', dkd_shipment_row.courier_fee_tl,
      'customer_charge_tl', dkd_shipment_row.customer_charge_tl,
      'payment_status', dkd_shipment_row.payment_status,
      'created_at', dkd_shipment_row.created_at,
      'accepted_at', dkd_shipment_row.accepted_at,
      'picked_up_at', dkd_shipment_row.picked_up_at,
      'completed_at', dkd_shipment_row.completed_at,
      'assigned_courier_user_id', dkd_shipment_row.assigned_courier_user_id,
      'courier_display_name', coalesce(nullif(trim(coalesce(dkd_profile_row.nickname, '')), ''), case when dkd_shipment_row.assigned_courier_user_id is null then '' else concat('Kurye ', substr(dkd_shipment_row.assigned_courier_user_id::text, 1, 6)) end),
      'courier_plate_no', coalesce(nullif(trim(coalesce(dkd_shipment_row.assigned_courier_plate_no, '')), ''), nullif(trim(coalesce(dkd_profile_row.courier_profile_meta->>'plate_no', '')), '')),
      'courier_vehicle_type', coalesce(nullif(trim(coalesce(dkd_shipment_row.assigned_courier_vehicle_type, '')), ''), nullif(trim(coalesce(dkd_profile_row.courier_vehicle_type, '')), '')),
      'courier_eta_min', dkd_shipment_row.courier_eta_min
    ) order by dkd_shipment_row.created_at desc
  ), '[]'::jsonb)
  into dkd_shipments_json_value
  from public.dkd_cargo_shipments dkd_shipment_row
  left join public.dkd_profiles dkd_profile_row
    on dkd_profile_row.user_id = dkd_shipment_row.assigned_courier_user_id
  where dkd_shipment_row.customer_user_id = dkd_user_id_value;

  return jsonb_build_object(
    'ok', true,
    'profile', coalesce(dkd_profile_json_value, '{}'::jsonb),
    'jobs', coalesce(dkd_jobs_json_value, '[]'::jsonb),
    'shipments', coalesce(dkd_shipments_json_value, '[]'::jsonb),
    'has_courier_license', dkd_has_courier_license_value,
    'server_time', now()
  );
end;
$dkd_snapshot$;

drop function if exists public.dkd_web_courier_job_accept_dkd(bigint);
create function public.dkd_web_courier_job_accept_dkd(
  dkd_param_job_id bigint default null
)
returns jsonb
language plpgsql
security definer
set search_path = public, auth
as $dkd_accept$
declare
  dkd_user_id_value uuid := auth.uid();
  dkd_has_courier_license_value boolean := false;
  dkd_job_id_value bigint := null;
  dkd_cargo_shipment_id_value bigint := null;
  dkd_order_id_value bigint := null;
  dkd_plate_value text := '';
  dkd_vehicle_value text := '';
begin
  if dkd_user_id_value is null then
    raise exception 'auth_required';
  end if;

  dkd_has_courier_license_value := public.dkd_web_courier_license_active_dkd(dkd_user_id_value);
  if dkd_has_courier_license_value is not true then
    return jsonb_build_object('ok', false, 'reason', 'courier_license_required');
  end if;

  insert into public.dkd_profiles (user_id)
  values (dkd_user_id_value)
  on conflict (user_id) do nothing;

  select coalesce(nullif(trim(coalesce(dkd_profile_row.courier_profile_meta->>'plate_no', '')), ''), ''),
         coalesce(nullif(trim(coalesce(dkd_profile_row.courier_vehicle_type, '')), ''), nullif(trim(coalesce(dkd_profile_row.courier_profile_meta->>'vehicle_type', '')), ''), 'moto')
  into dkd_plate_value, dkd_vehicle_value
  from public.dkd_profiles dkd_profile_row
  where dkd_profile_row.user_id = dkd_user_id_value;

  update public.dkd_courier_jobs
  set assigned_user_id = dkd_user_id_value,
      status = 'accepted',
      accepted_at = coalesce(accepted_at, now()),
      pickup_status = case when coalesce(pickup_status, 'pending') = 'pending' then 'accepted' else pickup_status end,
      updated_at = now()
  where public.dkd_courier_jobs.id = dkd_param_job_id
    and (public.dkd_courier_jobs.assigned_user_id is null or public.dkd_courier_jobs.assigned_user_id = dkd_user_id_value)
    and coalesce(public.dkd_courier_jobs.status, 'open') in ('open', 'ready', 'published', 'accepted')
  returning public.dkd_courier_jobs.id, public.dkd_courier_jobs.cargo_shipment_id, public.dkd_courier_jobs.order_id
  into dkd_job_id_value, dkd_cargo_shipment_id_value, dkd_order_id_value;

  if dkd_job_id_value is null then
    return jsonb_build_object('ok', false, 'reason', 'job_not_available');
  end if;

  if dkd_cargo_shipment_id_value is not null then
    update public.dkd_cargo_shipments
    set status = 'accepted',
        assigned_courier_user_id = dkd_user_id_value,
        assigned_courier_plate_no = nullif(dkd_plate_value, ''),
        assigned_courier_vehicle_type = nullif(dkd_vehicle_value, ''),
        accepted_at = coalesce(accepted_at, now()),
        courier_eta_min = coalesce(courier_eta_min, 18),
        updated_at = now()
    where id = dkd_cargo_shipment_id_value;
  end if;

  return jsonb_build_object('ok', true, 'job_id', dkd_job_id_value, 'cargo_shipment_id', dkd_cargo_shipment_id_value, 'order_id', dkd_order_id_value);
end;
$dkd_accept$;

