-- dkd web courier license gate + premium operation sync hotfix
create or replace function public.dkd_web_courier_license_active_dkd(
  dkd_param_user_id uuid default auth.uid()
)
returns boolean
language plpgsql
security definer
set search_path = public, auth
as $dkd_license_active$
declare
  dkd_user_id_value uuid := coalesce(dkd_param_user_id, auth.uid());
  dkd_profile_status_value text := 'none';
  dkd_application_status_value text := null;
begin
  if dkd_user_id_value is null then
    return false;
  end if;

  select coalesce(dkd_profile_row.courier_status, 'none')
    into dkd_profile_status_value
  from public.dkd_profiles as dkd_profile_row
  where dkd_profile_row.user_id = dkd_user_id_value
  limit 1;

  if to_regclass('public.dkd_courier_license_applications') is not null then
    execute
      'select coalesce(dkd_application_row.status, ''pending'')
         from public.dkd_courier_license_applications as dkd_application_row
        where dkd_application_row.user_id = $1
        order by dkd_application_row.updated_at desc nulls last, dkd_application_row.id desc
        limit 1'
      using dkd_user_id_value
      into dkd_application_status_value;
  end if;

  return lower(coalesce(dkd_profile_status_value, 'none')) in ('approved', 'active')
      or lower(coalesce(dkd_application_status_value, 'none')) in ('approved', 'active');
end;
$dkd_license_active$;

