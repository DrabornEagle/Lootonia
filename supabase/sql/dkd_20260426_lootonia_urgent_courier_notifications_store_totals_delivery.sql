  dkd_payload jsonb,
  dkd_dedupe_key text
)
language sql
security definer
set search_path = public
as $dkd_push_targets$
  with dkd_order_scope as (
    select
      dkd_order_row.*,
      coalesce(nullif(trim(dkd_order_row.dkd_customer_full_name), ''), 'Müşteri') as dkd_customer_name_value,
      coalesce(nullif(trim(dkd_courier_profile_row.nickname), ''), 'Kurye') as dkd_courier_name_value
    from public.dkd_urgent_courier_orders dkd_order_row
    left join public.dkd_profiles dkd_courier_profile_row
      on dkd_courier_profile_row.user_id = dkd_order_row.dkd_courier_user_id
    where dkd_order_row.dkd_order_id = dkd_param_order_id
  ),
  dkd_message_scope as (
    select
      dkd_message_row.dkd_message_id,
      dkd_message_row.dkd_sender_user_id,
      dkd_message_row.dkd_sender_role_key,
      dkd_message_row.dkd_message_text
    from public.dkd_urgent_courier_messages dkd_message_row
    where dkd_message_row.dkd_message_id = dkd_param_message_id
  ),
  dkd_target_scope as (
    select distinct
      case
        when dkd_param_event_key = 'dkd_urgent_order_created' then dkd_token_row.user_id
        when dkd_param_event_key = 'dkd_urgent_courier_accepted' then dkd_order_scope.dkd_customer_user_id
        when dkd_param_event_key = 'dkd_urgent_order_cancelled' then dkd_order_scope.dkd_customer_user_id
        when dkd_param_event_key = 'dkd_urgent_product_total_sent' then dkd_order_scope.dkd_customer_user_id
        when dkd_param_event_key = 'dkd_urgent_invoice_uploaded' then dkd_order_scope.dkd_customer_user_id
        when dkd_param_event_key = 'dkd_urgent_items_picked_up' then dkd_order_scope.dkd_customer_user_id
        when dkd_param_event_key = 'dkd_urgent_delivery_completed' then dkd_order_scope.dkd_customer_user_id
        when dkd_param_event_key = 'dkd_urgent_message_sent' and coalesce(dkd_message_scope.dkd_sender_role_key, '') = 'dkd_customer' then dkd_order_scope.dkd_courier_user_id
        when dkd_param_event_key = 'dkd_urgent_message_sent' and coalesce(dkd_message_scope.dkd_sender_role_key, '') = 'dkd_courier' then dkd_order_scope.dkd_customer_user_id
        when dkd_param_event_key = 'dkd_urgent_auto_message' then dkd_order_scope.dkd_customer_user_id
        else null::uuid
      end as dkd_target_user_id,
      dkd_order_scope.dkd_order_id,
      dkd_order_scope.dkd_customer_user_id,
      dkd_order_scope.dkd_courier_user_id,
      dkd_order_scope.dkd_customer_name_value,
      dkd_order_scope.dkd_courier_name_value,
      coalesce(dkd_message_scope.dkd_message_text, '') as dkd_message_text_value,
      coalesce(dkd_message_scope.dkd_sender_role_key, '') as dkd_sender_role_key_value
    from dkd_order_scope
    left join dkd_message_scope on true
    left join public.dkd_push_tokens dkd_token_row
      on dkd_param_event_key = 'dkd_urgent_order_created'
     and dkd_token_row.is_active = true
     and public.dkd_urgent_courier_license_active_dkd(dkd_token_row.user_id) is true
     and dkd_token_row.user_id <> dkd_order_scope.dkd_customer_user_id
  ),
  dkd_cancel_extra_scope as (
    select
      dkd_order_scope.dkd_courier_user_id as dkd_target_user_id,
      dkd_order_scope.dkd_order_id,
      dkd_order_scope.dkd_customer_user_id,
      dkd_order_scope.dkd_courier_user_id,
      dkd_order_scope.dkd_customer_name_value,
      dkd_order_scope.dkd_courier_name_value,
      ''::text as dkd_message_text_value,
      ''::text as dkd_sender_role_key_value
    from dkd_order_scope
    where dkd_param_event_key = 'dkd_urgent_order_cancelled'
      and dkd_order_scope.dkd_courier_user_id is not null
  ),
  dkd_auto_extra_scope as (
    select
      dkd_order_scope.dkd_courier_user_id as dkd_target_user_id,
      dkd_order_scope.dkd_order_id,
      dkd_order_scope.dkd_customer_user_id,
      dkd_order_scope.dkd_courier_user_id,
      dkd_order_scope.dkd_customer_name_value,
      dkd_order_scope.dkd_courier_name_value,
      coalesce(dkd_message_scope.dkd_message_text, '') as dkd_message_text_value,
      coalesce(dkd_message_scope.dkd_sender_role_key, '') as dkd_sender_role_key_value
    from dkd_order_scope
    left join dkd_message_scope on true
    where dkd_param_event_key = 'dkd_urgent_auto_message'
      and dkd_order_scope.dkd_courier_user_id is not null
  ),
  dkd_all_targets as (
    select * from dkd_target_scope
    union all
    select * from dkd_cancel_extra_scope
    union all
    select * from dkd_auto_extra_scope
  ),
  dkd_token_scope as (
    select
      dkd_all_targets.*,
      dkd_token_row.expo_push_token,
      row_number() over (
        partition by dkd_token_row.expo_push_token
        order by dkd_token_row.updated_at desc nulls last, dkd_token_row.id desc
      ) as dkd_token_rank
    from dkd_all_targets
    join public.dkd_push_tokens dkd_token_row
      on dkd_token_row.user_id = dkd_all_targets.dkd_target_user_id
     and dkd_token_row.is_active = true
     and nullif(trim(coalesce(dkd_token_row.expo_push_token, '')), '') is not null
    where dkd_all_targets.dkd_target_user_id is not null
  )
  select
    dkd_token_scope.dkd_target_user_id,
    dkd_token_scope.expo_push_token,
    case
      when dkd_param_event_key = 'dkd_urgent_order_created' then 'Yeni Acil Kurye Siparişi'
      when dkd_param_event_key = 'dkd_urgent_courier_accepted' then 'Kurye Görevi Kabul Etti'
      when dkd_param_event_key = 'dkd_urgent_order_cancelled' then 'Acil Kurye Siparişi İptal Edildi'
      when dkd_param_event_key = 'dkd_urgent_product_total_sent' then 'Ürün Toplamı Girildi'
      when dkd_param_event_key = 'dkd_urgent_invoice_uploaded' then 'Fatura Görseli Yüklendi'
      when dkd_param_event_key = 'dkd_urgent_items_picked_up' then 'Kurye Yola Çıktı'
      when dkd_param_event_key = 'dkd_urgent_delivery_completed' then 'Acil Kurye Teslim Edildi'
      when dkd_param_event_key = 'dkd_urgent_message_sent' then 'Acil Kurye Mesajı'
      when dkd_param_event_key = 'dkd_urgent_auto_message' then 'Acil Kurye Canlı Güncelleme'
      else 'Acil Kurye'
    end as dkd_title,
    case
      when dkd_param_event_key = 'dkd_urgent_order_created' then concat(dkd_token_scope.dkd_customer_name_value, ' yeni acil kurye siparişi oluşturdu.')
      when dkd_param_event_key = 'dkd_urgent_courier_accepted' then concat(dkd_token_scope.dkd_courier_name_value, ' görevi kabul etti ve taşıma ücretini gönderdi.')
      when dkd_param_event_key = 'dkd_urgent_order_cancelled' then concat(dkd_token_scope.dkd_customer_name_value, ' / Acil Kurye siparişi iptal edildi.')
      when dkd_param_event_key = 'dkd_urgent_product_total_sent' then 'Kurye mağaza bazlı ürün toplamlarını girdi. Onay bekliyor.'
      when dkd_param_event_key = 'dkd_urgent_invoice_uploaded' then 'Kurye faturayı yükledi. Görseli sipariş detayından inceleyebilirsin.'
      when dkd_param_event_key = 'dkd_urgent_items_picked_up' then 'Kurye ürünleri teslim aldı ve sana doğru yola çıktı.'
      when dkd_param_event_key = 'dkd_urgent_delivery_completed' then 'Kurye siparişi teslim etti ve işi tamamladı.'
      when dkd_param_event_key = 'dkd_urgent_message_sent' then left(coalesce(dkd_token_scope.dkd_message_text_value, 'Yeni mesajın var.'), 120)
      when dkd_param_event_key = 'dkd_urgent_auto_message' then left(coalesce(dkd_token_scope.dkd_message_text_value, 'Acil Kurye akışında yeni otomatik güncelleme var.'), 120)
      else 'Acil Kurye akışında yeni güncelleme var.'
    end as dkd_body,
    jsonb_build_object(
      'route', 'courier',
      'targetScreen', 'courier',
      'dkd_event_key', dkd_param_event_key,
      'dkd_urgent_courier_order_id', dkd_token_scope.dkd_order_id,
      'dkd_message_id', dkd_param_message_id,
      'dkd_push_segment', 'urgent_courier'
    ) as dkd_payload,
    concat(
      dkd_param_event_key,
      ':',
      dkd_token_scope.dkd_order_id::text,
      ':',
      coalesce(dkd_param_message_id::text, 'none'),
      ':',
      dkd_token_scope.dkd_target_user_id::text,
      ':',
      md5(dkd_token_scope.expo_push_token)
    ) as dkd_dedupe_key
  from dkd_token_scope
  where dkd_token_scope.dkd_token_rank = 1;
$dkd_push_targets$;

revoke all on function public.dkd_urgent_courier_set_item_totals_dkd(uuid, jsonb) from public;
revoke all on function public.dkd_urgent_courier_pickup_items_dkd(uuid) from public;
revoke all on function public.dkd_urgent_courier_complete_delivery_dkd(uuid) from public;
revoke all on function public.dkd_urgent_courier_push_targets_dkd(uuid, text, uuid) from public;

grant execute on function public.dkd_urgent_courier_order_json_dkd(uuid) to authenticated, service_role;
grant execute on function public.dkd_urgent_courier_set_product_total_dkd(uuid, numeric) to authenticated;
grant execute on function public.dkd_urgent_courier_set_item_totals_dkd(uuid, jsonb) to authenticated;
grant execute on function public.dkd_urgent_courier_customer_cancel_order_dkd(uuid) to authenticated;
grant execute on function public.dkd_urgent_courier_send_message_dkd(uuid, text) to authenticated;
grant execute on function public.dkd_urgent_courier_pickup_items_dkd(uuid) to authenticated;
grant execute on function public.dkd_urgent_courier_complete_delivery_dkd(uuid) to authenticated;
grant execute on function public.dkd_urgent_courier_push_targets_dkd(uuid, text, uuid) to authenticated, service_role;

grant select, insert, update on public.dkd_urgent_courier_push_audit to service_role;

