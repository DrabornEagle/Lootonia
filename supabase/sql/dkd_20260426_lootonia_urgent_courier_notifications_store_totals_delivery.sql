create or replace function public.dkd_urgent_courier_set_item_totals_dkd(
  dkd_param_order_id uuid,
  dkd_param_item_totals jsonb default '[]'::jsonb
)
returns jsonb
language plpgsql
security definer
set search_path = public, auth
as $dkd_set_item_totals$
declare
  dkd_user_id_value uuid := auth.uid();
  dkd_payload_value jsonb := coalesce(dkd_param_item_totals, '[]'::jsonb);
  dkd_sum_value numeric(12,2) := 0;
  dkd_order_item_count_value integer := 0;
  dkd_payload_item_count_value integer := 0;
  dkd_updated_count_value integer := 0;
begin
  if dkd_user_id_value is null then
    raise exception 'auth_required';
  end if;
  if jsonb_typeof(dkd_payload_value) <> 'array' then
    raise exception 'item_totals_required';
  end if;

  if not exists (
    select 1
    from public.dkd_urgent_courier_orders dkd_order_check_row
    where dkd_order_check_row.dkd_order_id = dkd_param_order_id
      and dkd_order_check_row.dkd_courier_user_id = dkd_user_id_value
      and dkd_order_check_row.dkd_status_key in ('dkd_fee_paid_shopping', 'dkd_product_total_waiting', 'dkd_product_total_approved')
  ) then
    return jsonb_build_object('dkd_ok', false, 'dkd_reason', 'order_not_ready');
  end if;

  select count(*)
  into dkd_order_item_count_value
  from public.dkd_urgent_courier_order_items dkd_item_row
  where dkd_item_row.dkd_order_id = dkd_param_order_id;

  select count(*)
  into dkd_payload_item_count_value
  from jsonb_array_elements(dkd_payload_value) dkd_payload_row(dkd_payload_item)
  where coalesce((dkd_payload_row.dkd_payload_item ->> 'dkd_product_total_tl')::numeric, 0) > 0;

  if dkd_order_item_count_value <= 0 or dkd_payload_item_count_value < dkd_order_item_count_value then
    return jsonb_build_object('dkd_ok', false, 'dkd_reason', 'all_store_totals_required');
  end if;

  update public.dkd_urgent_courier_order_items dkd_item_row
  set dkd_product_total_tl = round(greatest(coalesce((dkd_payload_source_value.dkd_payload_item ->> 'dkd_product_total_tl')::numeric, 0), 0), 2)
  from jsonb_array_elements(dkd_payload_value) dkd_payload_source_value(dkd_payload_item)
  where dkd_item_row.dkd_order_id = dkd_param_order_id
    and dkd_item_row.dkd_item_id = (dkd_payload_source_value.dkd_payload_item ->> 'dkd_item_id')::uuid;

  get diagnostics dkd_updated_count_value = row_count;

  if dkd_updated_count_value < dkd_order_item_count_value then
    return jsonb_build_object('dkd_ok', false, 'dkd_reason', 'all_store_totals_required');
  end if;

  select round(coalesce(sum(dkd_item_row.dkd_product_total_tl), 0), 2)
  into dkd_sum_value
  from public.dkd_urgent_courier_order_items dkd_item_row
  where dkd_item_row.dkd_order_id = dkd_param_order_id;

  update public.dkd_urgent_courier_orders
  set dkd_product_total_tl = dkd_sum_value,
      dkd_product_total_submitted_at = now(),
      dkd_status_key = 'dkd_product_total_waiting'
  where dkd_order_id = dkd_param_order_id
    and dkd_courier_user_id = dkd_user_id_value
    and dkd_status_key in ('dkd_fee_paid_shopping', 'dkd_product_total_waiting', 'dkd_product_total_approved');

  get diagnostics dkd_updated_count_value = row_count;
  if dkd_updated_count_value <= 0 then
    return jsonb_build_object('dkd_ok', false, 'dkd_reason', 'order_not_ready');
  end if;

  insert into public.dkd_urgent_courier_messages (dkd_order_id, dkd_sender_user_id, dkd_sender_role_key, dkd_message_text)
  values (dkd_param_order_id, dkd_user_id_value, 'dkd_courier', concat('Mağaza bazlı ürün toplamları girildi. Genel toplam: ', dkd_sum_value::text, ' TL'));

  return jsonb_build_object('dkd_ok', true, 'dkd_order_id', dkd_param_order_id, 'dkd_product_total_tl', dkd_sum_value);
end;
$dkd_set_item_totals$;

create or replace function public.dkd_urgent_courier_set_product_total_dkd(
  dkd_param_order_id uuid,
  dkd_param_product_total_tl numeric
)
returns jsonb
language plpgsql
security definer
set search_path = public, auth
as $dkd_set_product_total$
declare
  dkd_user_id_value uuid := auth.uid();
  dkd_total_value numeric(12,2) := round(greatest(coalesce(dkd_param_product_total_tl, 0), 0), 2);
  dkd_item_count_value integer := 0;
  dkd_updated_count_value integer := 0;
begin
  if dkd_user_id_value is null then
    raise exception 'auth_required';
  end if;
  if dkd_total_value <= 0 then
    raise exception 'product_total_required';
  end if;

  if not exists (
    select 1
    from public.dkd_urgent_courier_orders dkd_order_check_row
    where dkd_order_check_row.dkd_order_id = dkd_param_order_id
      and dkd_order_check_row.dkd_courier_user_id = dkd_user_id_value
      and dkd_order_check_row.dkd_status_key in ('dkd_fee_paid_shopping', 'dkd_product_total_waiting', 'dkd_product_total_approved')
  ) then
    return jsonb_build_object('dkd_ok', false, 'dkd_reason', 'order_not_ready');
  end if;

  select count(*)
  into dkd_item_count_value
  from public.dkd_urgent_courier_order_items dkd_item_row
  where dkd_item_row.dkd_order_id = dkd_param_order_id;

  if dkd_item_count_value <= 1 then
    update public.dkd_urgent_courier_order_items
    set dkd_product_total_tl = dkd_total_value
    where dkd_order_id = dkd_param_order_id;
  end if;

  update public.dkd_urgent_courier_orders
  set dkd_product_total_tl = dkd_total_value,
      dkd_product_total_submitted_at = now(),
      dkd_status_key = 'dkd_product_total_waiting'
  where dkd_order_id = dkd_param_order_id
    and dkd_courier_user_id = dkd_user_id_value
    and dkd_status_key in ('dkd_fee_paid_shopping', 'dkd_product_total_waiting', 'dkd_product_total_approved')
    and dkd_item_count_value <= 1;

  get diagnostics dkd_updated_count_value = row_count;
  if dkd_updated_count_value <= 0 then
    return jsonb_build_object('dkd_ok', false, 'dkd_reason', 'store_based_totals_required');
  end if;

  insert into public.dkd_urgent_courier_messages (dkd_order_id, dkd_sender_user_id, dkd_sender_role_key, dkd_message_text)
  values (dkd_param_order_id, dkd_user_id_value, 'dkd_courier', concat('Ürünlerin toplam TL fiyatı girildi: ', dkd_total_value::text, ' TL'));

  return jsonb_build_object('dkd_ok', true, 'dkd_order_id', dkd_param_order_id, 'dkd_product_total_tl', dkd_total_value);
end;
$dkd_set_product_total$;

create or replace function public.dkd_urgent_courier_customer_cancel_order_dkd(
  dkd_param_order_id uuid
)
returns jsonb
language plpgsql
security definer
set search_path = public, auth
as $dkd_cancel_order$
declare
  dkd_user_id_value uuid := auth.uid();
  dkd_order_row_value public.dkd_urgent_courier_orders%rowtype;
  dkd_message_value text := 'Acil Kurye siparişi iptal edildi.';
begin
  if dkd_user_id_value is null then
    raise exception 'auth_required';
  end if;

  select *
  into dkd_order_row_value
  from public.dkd_urgent_courier_orders
  where dkd_order_id = dkd_param_order_id
  for update;

  if dkd_order_row_value.dkd_order_id is null then
    return jsonb_build_object('dkd_ok', false, 'dkd_reason', 'order_not_found');
  end if;
  if dkd_order_row_value.dkd_status_key in ('dkd_completed', 'dkd_cancelled') then
    return jsonb_build_object('dkd_ok', false, 'dkd_reason', 'cancel_not_available');
  end if;
  if dkd_user_id_value <> dkd_order_row_value.dkd_customer_user_id
     and (dkd_order_row_value.dkd_courier_user_id is null or dkd_user_id_value <> dkd_order_row_value.dkd_courier_user_id) then
    return jsonb_build_object('dkd_ok', false, 'dkd_reason', 'not_order_member');
  end if;

  if dkd_user_id_value = dkd_order_row_value.dkd_customer_user_id then
    dkd_message_value := 'Müşteri Acil Kurye siparişini iptal etti.';
  else
    dkd_message_value := 'Kurye Acil Kurye siparişini iptal etti.';
  end if;

  update public.dkd_urgent_courier_orders
  set dkd_status_key = 'dkd_cancelled',
      dkd_chat_enabled_value = false
  where dkd_order_id = dkd_param_order_id;

  insert into public.dkd_urgent_courier_messages (dkd_order_id, dkd_sender_user_id, dkd_sender_role_key, dkd_message_text)
  values (
    dkd_param_order_id,
    dkd_user_id_value,
    case when dkd_user_id_value = dkd_order_row_value.dkd_customer_user_id then 'dkd_customer' else 'dkd_courier' end,
    dkd_message_value
  );

  return jsonb_build_object('dkd_ok', true, 'dkd_order_id', dkd_param_order_id);
end;
$dkd_cancel_order$;

create or replace function public.dkd_urgent_courier_send_message_dkd(
  dkd_param_order_id uuid,
  dkd_param_message_text text default null
)
returns jsonb
language plpgsql
security definer
set search_path = public, auth
as $dkd_send_message$
declare
  dkd_user_id_value uuid := auth.uid();
  dkd_order_row_value public.dkd_urgent_courier_orders%rowtype;
  dkd_message_value text := trim(coalesce(dkd_param_message_text, ''));
  dkd_role_key_value text := 'dkd_customer';
  dkd_message_id_value uuid;
begin
  if dkd_user_id_value is null then
    raise exception 'auth_required';
  end if;
  if length(dkd_message_value) < 1 then
    raise exception 'message_required';
  end if;

  select *
  into dkd_order_row_value
  from public.dkd_urgent_courier_orders
  where dkd_order_id = dkd_param_order_id;

  if dkd_order_row_value.dkd_order_id is null then
    raise exception 'order_not_found';
  end if;
  if dkd_order_row_value.dkd_chat_enabled_value is not true then
    raise exception 'chat_not_enabled';
  end if;
  if dkd_order_row_value.dkd_status_key in ('dkd_cancelled', 'dkd_completed') then
    raise exception 'chat_closed';
  end if;
  if dkd_user_id_value = dkd_order_row_value.dkd_customer_user_id then
    dkd_role_key_value := 'dkd_customer';
  elsif dkd_user_id_value = dkd_order_row_value.dkd_courier_user_id then
    dkd_role_key_value := 'dkd_courier';
  else
    raise exception 'not_order_member';
  end if;

  insert into public.dkd_urgent_courier_messages (dkd_order_id, dkd_sender_user_id, dkd_sender_role_key, dkd_message_text)
  values (dkd_param_order_id, dkd_user_id_value, dkd_role_key_value, dkd_message_value)
  returning dkd_message_id into dkd_message_id_value;

  return jsonb_build_object('dkd_ok', true, 'dkd_order_id', dkd_param_order_id, 'dkd_message_id', dkd_message_id_value);
end;
$dkd_send_message$;

