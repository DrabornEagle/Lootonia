create or replace view public.dkd_business_context_by_drop as
select
  dkd_alias_drop_link.drop_id,
  dkd_alias_drop_link.business_id,
  dkd_alias_business.name as business_name,
  dkd_alias_drop_link.is_primary,
  dkd_alias_drop_link.traffic_weight,
  dkd_alias_campaign.id as campaign_id,
  dkd_alias_campaign.title as campaign_title,
  coalesce(dkd_alias_campaign.reward_label, dkd_alias_campaign.coupon_reward_label, 'ÖDÜL') as reward_badge_label,
  greatest(0, coalesce(nullif(dkd_alias_campaign.stock_left, 0), dkd_alias_campaign.stock_limit, 0) - coalesce(dkd_alias_campaign.redeemed_count, 0))::int as campaign_stock_left,
  coalesce(dkd_alias_campaign.closes_at, dkd_alias_campaign.ends_at) as campaign_expires_at,
  coalesce(dkd_alias_today.unique_players, 0)::int as campaign_players_today
from public.dkd_business_drop_links dkd_alias_drop_link
join public.dkd_businesses dkd_alias_business
  on dkd_alias_business.id = dkd_alias_drop_link.business_id
left join lateral (
  select *
  from public.dkd_business_campaigns dkd_alias_campaign_inner
  where dkd_alias_campaign_inner.business_id = dkd_alias_drop_link.business_id
    and coalesce(dkd_alias_campaign_inner.is_active, true) = true
    and (dkd_alias_campaign_inner.starts_at is null or dkd_alias_campaign_inner.starts_at <= now())
    and (dkd_alias_campaign_inner.ends_at is null or dkd_alias_campaign_inner.ends_at >= now())
    and (dkd_alias_campaign_inner.closes_at is null or dkd_alias_campaign_inner.closes_at >= now())
  order by dkd_alias_campaign_inner.updated_at desc, dkd_alias_campaign_inner.created_at desc
  limit 1
) dkd_alias_campaign on true
left join public.dkd_business_today_metrics dkd_alias_today
  on dkd_alias_today.business_id = dkd_alias_drop_link.business_id;

