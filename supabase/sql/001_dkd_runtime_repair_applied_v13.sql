      nullif(trim(coalesce(dkd_param_business_key, '')), ''),
      nullif(trim(coalesce(dkd_param_slug, dkd_slug, '')), ''),
      trim(coalesce(dkd_param_name, dkd_name)),
      nullif(trim(coalesce(dkd_param_category, dkd_category, '')), ''),
