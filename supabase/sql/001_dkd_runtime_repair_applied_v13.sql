      business_key,
      slug,
      name,
      category,
      city,
      district,
      address_text,
      sponsor_name,
      sponsor_enabled,
      opens_at,
      closes_at,
      daily_scan_goal,
      stock_alert_threshold,
      is_active,
      lat,
      lng,
      radius_m,
      meta
