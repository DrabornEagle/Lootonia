create or replace function public.dkd_social_snapshot()
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  dkd_var_user_id uuid := auth.uid();
  dkd_var_my_profile jsonb;
  dkd_var_friends jsonb;
  dkd_var_incoming jsonb;
  dkd_var_outgoing jsonb;
begin
  if dkd_var_user_id is null then
    return jsonb_build_object('myProfile', null, 'friends', '[]'::jsonb, 'incoming', '[]'::jsonb, 'outgoing', '[]'::jsonb);
  end if;

  perform public.dkd_social_touch_presence();

  select to_jsonb(public.dkd_profiles.*)
  into dkd_var_my_profile
  from public.dkd_profiles
  where public.dkd_profiles.user_id = dkd_var_user_id;

  with dkd_friend_rows as (
    select
      public.dkd_social_friendships.thread_id,
      case
        when public.dkd_social_friendships.user_low = dkd_var_user_id then public.dkd_social_friendships.user_high
        else public.dkd_social_friendships.user_low
      end as friend_user_id
    from public.dkd_social_friendships
    where public.dkd_social_friendships.user_low = dkd_var_user_id
       or public.dkd_social_friendships.user_high = dkd_var_user_id
  ),
  dkd_friend_payload as (
    select
      dkd_friend_rows.friend_user_id as user_id,
      dkd_friend_rows.thread_id,
      public.dkd_profiles.ally_id,
      public.dkd_profiles.nickname,
      public.dkd_profiles.avatar_emoji,
      public.dkd_profiles.social_last_seen_at,
      public.dkd_social_thread_members.last_seen_at as my_last_seen_at,
      (
        select public.dkd_social_messages.body
        from public.dkd_social_messages
        where public.dkd_social_messages.thread_id = dkd_friend_rows.thread_id
        order by public.dkd_social_messages.created_at desc, public.dkd_social_messages.id desc
        limit 1
      ) as last_message_body,
      (
        select public.dkd_social_messages.created_at
        from public.dkd_social_messages
        where public.dkd_social_messages.thread_id = dkd_friend_rows.thread_id
        order by public.dkd_social_messages.created_at desc, public.dkd_social_messages.id desc
        limit 1
      ) as last_message_at,
      (
        select count(*)::integer
        from public.dkd_social_messages
        where public.dkd_social_messages.thread_id = dkd_friend_rows.thread_id
          and public.dkd_social_messages.sender_user_id <> dkd_var_user_id
          and public.dkd_social_messages.created_at > coalesce(public.dkd_social_thread_members.last_seen_at, 'epoch'::timestamptz)
      ) as unread_count
    from dkd_friend_rows
    join public.dkd_profiles
      on public.dkd_profiles.user_id = dkd_friend_rows.friend_user_id
    left join public.dkd_social_thread_members
      on public.dkd_social_thread_members.thread_id = dkd_friend_rows.thread_id
     and public.dkd_social_thread_members.user_id = dkd_var_user_id
  )
  select coalesce(jsonb_agg(jsonb_build_object(
    'user_id', dkd_friend_payload.user_id,
    'thread_id', dkd_friend_payload.thread_id,
    'ally_id', dkd_friend_payload.ally_id,
    'nickname', dkd_friend_payload.nickname,
    'avatar_emoji', dkd_friend_payload.avatar_emoji,
    'social_last_seen_at', dkd_friend_payload.social_last_seen_at,
    'online', (dkd_friend_payload.social_last_seen_at is not null and dkd_friend_payload.social_last_seen_at >= now() - interval '90 seconds'),
    'unread_count', coalesce(dkd_friend_payload.unread_count, 0),
    'last_message_body', dkd_friend_payload.last_message_body,
    'last_message_at', dkd_friend_payload.last_message_at
  ) order by dkd_friend_payload.last_message_at desc nulls last, dkd_friend_payload.nickname asc nulls last), '[]'::jsonb)
  into dkd_var_friends
  from dkd_friend_payload;

  select coalesce(jsonb_agg(jsonb_build_object(
    'id', public.dkd_social_friend_requests.id,
    'requester_user_id', public.dkd_social_friend_requests.requester_user_id,
    'ally_id', public.dkd_profiles.ally_id,
    'nickname', public.dkd_profiles.nickname,
    'avatar_emoji', public.dkd_profiles.avatar_emoji,
    'created_at', public.dkd_social_friend_requests.created_at
  ) order by public.dkd_social_friend_requests.created_at desc), '[]'::jsonb)
  into dkd_var_incoming
  from public.dkd_social_friend_requests
  join public.dkd_profiles
    on public.dkd_profiles.user_id = public.dkd_social_friend_requests.requester_user_id
  where public.dkd_social_friend_requests.target_user_id = dkd_var_user_id
    and public.dkd_social_friend_requests.status = 'pending';

  select coalesce(jsonb_agg(jsonb_build_object(
    'id', public.dkd_social_friend_requests.id,
    'target_user_id', public.dkd_social_friend_requests.target_user_id,
    'ally_id', public.dkd_profiles.ally_id,
    'nickname', public.dkd_profiles.nickname,
    'avatar_emoji', public.dkd_profiles.avatar_emoji,
    'created_at', public.dkd_social_friend_requests.created_at
  ) order by public.dkd_social_friend_requests.created_at desc), '[]'::jsonb)
  into dkd_var_outgoing
  from public.dkd_social_friend_requests
  join public.dkd_profiles
    on public.dkd_profiles.user_id = public.dkd_social_friend_requests.target_user_id
  where public.dkd_social_friend_requests.requester_user_id = dkd_var_user_id
    and public.dkd_social_friend_requests.status = 'pending';

  return jsonb_build_object(
    'myProfile', coalesce(dkd_var_my_profile, '{}'::jsonb),
    'friends', coalesce(dkd_var_friends, '[]'::jsonb),
    'incoming', coalesce(dkd_var_incoming, '[]'::jsonb),
    'outgoing', coalesce(dkd_var_outgoing, '[]'::jsonb)
  );
end;
$$;

