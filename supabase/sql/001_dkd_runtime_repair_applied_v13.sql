      greatest(coalesce(dkd_param_stock_alert_threshold, 0), 0),
      coalesce(dkd_param_is_active, dkd_is_active, true),
      coalesce(dkd_param_lat, dkd_lat),
      coalesce(dkd_param_lng, dkd_lng),
      greatest(coalesce(dkd_param_radius_m, dkd_radius_m, 80), 20),
      coalesce(dkd_param_meta, dkd_meta, '{}'::jsonb)
    ) returning public.dkd_businesses.id into dkd_var_row_id;
  else
    update public.dkd_businesses
    set
      business_key = coalesce(nullif(trim(coalesce(dkd_param_business_key, '')), ''), public.dkd_businesses.business_key),
      slug = coalesce(nullif(trim(coalesce(dkd_param_slug, dkd_slug, '')), ''), public.dkd_businesses.slug),
      name = coalesce(nullif(trim(coalesce(dkd_param_name, dkd_name, '')), ''), public.dkd_businesses.name),
      category = coalesce(nullif(trim(coalesce(dkd_param_category, dkd_category, '')), ''), public.dkd_businesses.category),
      city = coalesce(nullif(trim(coalesce(dkd_param_city, dkd_city, '')), ''), public.dkd_businesses.city),
      district = case when coalesce(dkd_param_district, dkd_district) is null then public.dkd_businesses.district else nullif(trim(coalesce(dkd_param_district, dkd_district, '')), '') end,
      address_text = case when coalesce(dkd_param_address_text, dkd_address_text) is null then public.dkd_businesses.address_text else nullif(trim(coalesce(dkd_param_address_text, dkd_address_text, '')), '') end,
      sponsor_name = case when coalesce(dkd_param_sponsor_name, dkd_sponsor_name) is null then public.dkd_businesses.sponsor_name else nullif(trim(coalesce(dkd_param_sponsor_name, dkd_sponsor_name, '')), '') end,
      sponsor_enabled = coalesce(dkd_param_sponsor_enabled, public.dkd_businesses.sponsor_enabled),
      opens_at = case when coalesce(dkd_param_opens_at, dkd_opens_at) is null then public.dkd_businesses.opens_at else nullif(trim(coalesce(dkd_param_opens_at, dkd_opens_at, '')), '') end,
      closes_at = case when coalesce(dkd_param_closes_at, dkd_closes_at) is null then public.dkd_businesses.closes_at else nullif(trim(coalesce(dkd_param_closes_at, dkd_closes_at, '')), '') end,
