create or replace function public.dkd_social_send_friend_request(
  dkd_param_target_user_id uuid default null
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  dkd_var_user_id uuid := auth.uid();
  dkd_var_existing_request public.dkd_social_friend_requests%rowtype;
  dkd_var_reverse_request public.dkd_social_friend_requests%rowtype;
  dkd_var_low_user_id uuid;
  dkd_var_high_user_id uuid;
  dkd_var_thread_id uuid;
begin
  if dkd_var_user_id is null then
    return jsonb_build_object('ok', false, 'reason', 'auth_required');
  end if;

  if dkd_param_target_user_id is null then
    return jsonb_build_object('ok', false, 'reason', 'target_not_found');
  end if;

  if dkd_param_target_user_id = dkd_var_user_id then
    return jsonb_build_object('ok', false, 'reason', 'cannot_friend_self');
  end if;

  if not exists (select 1 from public.dkd_profiles where public.dkd_profiles.user_id = dkd_param_target_user_id) then
    return jsonb_build_object('ok', false, 'reason', 'target_not_found');
  end if;

  dkd_var_low_user_id := least(dkd_var_user_id, dkd_param_target_user_id);
  dkd_var_high_user_id := greatest(dkd_var_user_id, dkd_param_target_user_id);

  if exists (
    select 1
    from public.dkd_social_friendships
    where public.dkd_social_friendships.user_low = dkd_var_low_user_id
      and public.dkd_social_friendships.user_high = dkd_var_high_user_id
  ) then
    return jsonb_build_object('ok', false, 'reason', 'friend_exists');
  end if;

  select * into dkd_var_reverse_request
  from public.dkd_social_friend_requests
  where public.dkd_social_friend_requests.requester_user_id = dkd_param_target_user_id
    and public.dkd_social_friend_requests.target_user_id = dkd_var_user_id
    and public.dkd_social_friend_requests.status = 'pending'
  order by public.dkd_social_friend_requests.id desc
  limit 1;

  if dkd_var_reverse_request.id is not null then
    insert into public.dkd_social_threads (kind)
    values ('dm')
    returning public.dkd_social_threads.id into dkd_var_thread_id;

    insert into public.dkd_social_thread_members (thread_id, user_id, last_seen_at)
    values
      (dkd_var_thread_id, dkd_var_user_id, now()),
      (dkd_var_thread_id, dkd_param_target_user_id, now())
    on conflict (thread_id, user_id) do update
    set last_seen_at = excluded.last_seen_at;

    insert into public.dkd_social_friendships (user_low, user_high, thread_id)
    values (dkd_var_low_user_id, dkd_var_high_user_id, dkd_var_thread_id)
    on conflict (user_low, user_high) do update
    set thread_id = coalesce(public.dkd_social_friendships.thread_id, excluded.thread_id);

    update public.dkd_social_friend_requests
    set status = 'accepted',
        updated_at = now()
    where public.dkd_social_friend_requests.id = dkd_var_reverse_request.id;

    return jsonb_build_object('ok', true, 'reason', 'accepted_existing', 'thread_id', dkd_var_thread_id);
  end if;

  select * into dkd_var_existing_request
  from public.dkd_social_friend_requests
  where public.dkd_social_friend_requests.requester_user_id = dkd_var_user_id
    and public.dkd_social_friend_requests.target_user_id = dkd_param_target_user_id
    and public.dkd_social_friend_requests.status = 'pending'
  order by public.dkd_social_friend_requests.id desc
  limit 1;

  if dkd_var_existing_request.id is not null then
    return jsonb_build_object('ok', false, 'reason', 'request_exists');
  end if;

  insert into public.dkd_social_friend_requests (requester_user_id, target_user_id, status)
  values (dkd_var_user_id, dkd_param_target_user_id, 'pending');

  return jsonb_build_object('ok', true, 'reason', 'sent');
end;
$$;

grant execute on function public.dkd_social_send_friend_request(uuid) to authenticated;

create or replace function public.dkd_social_respond_friend_request(
  dkd_param_request_id bigint default null,
  dkd_param_action text default null
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  dkd_var_user_id uuid := auth.uid();
  dkd_var_request public.dkd_social_friend_requests%rowtype;
  dkd_var_action text := lower(trim(coalesce(dkd_param_action, 'accept')));
  dkd_var_low_user_id uuid;
  dkd_var_high_user_id uuid;
  dkd_var_thread_id uuid;
begin
  if dkd_var_user_id is null then
    return jsonb_build_object('ok', false, 'reason', 'auth_required');
  end if;

  select * into dkd_var_request
  from public.dkd_social_friend_requests
  where public.dkd_social_friend_requests.id = dkd_param_request_id
  limit 1;

  if dkd_var_request.id is null then
    return jsonb_build_object('ok', false, 'reason', 'request_not_found');
  end if;

  if dkd_var_request.target_user_id <> dkd_var_user_id then
    return jsonb_build_object('ok', false, 'reason', 'not_request_target');
  end if;

  if dkd_var_request.status <> 'pending' then
    return jsonb_build_object('ok', false, 'reason', 'request_already_closed');
  end if;

  if dkd_var_action not in ('accept', 'reject', 'decline') then
    dkd_var_action := 'accept';
  end if;

  if dkd_var_action in ('reject', 'decline') then
    update public.dkd_social_friend_requests
    set status = 'declined',
        updated_at = now()
    where public.dkd_social_friend_requests.id = dkd_var_request.id;

    return jsonb_build_object('ok', true, 'reason', 'declined');
  end if;

  dkd_var_low_user_id := least(dkd_var_request.requester_user_id, dkd_var_request.target_user_id);
  dkd_var_high_user_id := greatest(dkd_var_request.requester_user_id, dkd_var_request.target_user_id);

  select public.dkd_social_friendships.thread_id
  into dkd_var_thread_id
  from public.dkd_social_friendships
  where public.dkd_social_friendships.user_low = dkd_var_low_user_id
    and public.dkd_social_friendships.user_high = dkd_var_high_user_id
  limit 1;

  if dkd_var_thread_id is null then
    insert into public.dkd_social_threads (kind)
    values ('dm')
    returning public.dkd_social_threads.id into dkd_var_thread_id;
  end if;

  insert into public.dkd_social_thread_members (thread_id, user_id, last_seen_at)
  values
    (dkd_var_thread_id, dkd_var_request.requester_user_id, now()),
    (dkd_var_thread_id, dkd_var_request.target_user_id, now())
  on conflict (thread_id, user_id) do update
  set last_seen_at = excluded.last_seen_at;

  insert into public.dkd_social_friendships (user_low, user_high, thread_id)
  values (dkd_var_low_user_id, dkd_var_high_user_id, dkd_var_thread_id)
  on conflict (user_low, user_high) do update
  set thread_id = coalesce(public.dkd_social_friendships.thread_id, excluded.thread_id);

  update public.dkd_social_friend_requests
  set status = 'accepted',
      updated_at = now()
  where public.dkd_social_friend_requests.id = dkd_var_request.id;

  return jsonb_build_object('ok', true, 'reason', 'accepted', 'thread_id', dkd_var_thread_id);
end;
$$;

