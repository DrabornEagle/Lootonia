      stock_alert_threshold = greatest(coalesce(dkd_param_stock_alert_threshold, public.dkd_businesses.stock_alert_threshold), 0),
      is_active = coalesce(dkd_param_is_active, dkd_is_active, public.dkd_businesses.is_active),
