  end if;

  if nullif(trim(coalesce(dkd_param_token, '')), '') is null then
    return jsonb_build_object('ok', false, 'reason', 'token_required');
  end if;

  insert into public.dkd_push_tokens (user_id, token, platform, app_mode, device_name, is_active, created_at, updated_at)
  values (
    dkd_var_user_id,
    trim(dkd_param_token),
    nullif(trim(coalesce(dkd_param_platform, '')), ''),
    nullif(trim(coalesce(dkd_param_app_mode, '')), ''),
    nullif(trim(coalesce(dkd_param_device_name, '')), ''),
    true,
    now(),
    now()
  )
  on conflict (token) do update
  set user_id = excluded.user_id,
      platform = coalesce(excluded.platform, public.dkd_push_tokens.platform),
      app_mode = coalesce(excluded.app_mode, public.dkd_push_tokens.app_mode),
      device_name = coalesce(excluded.device_name, public.dkd_push_tokens.device_name),
      is_active = true,
      updated_at = now();

  return jsonb_build_object('ok', true, 'reason', 'upserted');
end;
$$;

grant execute on function public.dkd_upsert_push_token(text, text, text, text) to authenticated;

create or replace function public.dkd_disable_push_token(
  dkd_param_token text default null
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  dkd_var_user_id uuid := auth.uid();
begin
  if dkd_var_user_id is null then
    return jsonb_build_object('ok', false, 'reason', 'auth_required');
  end if;

  update public.dkd_push_tokens
  set is_active = false,
      updated_at = now()
  where public.dkd_push_tokens.token = trim(coalesce(dkd_param_token, ''))
    and public.dkd_push_tokens.user_id = dkd_var_user_id;

  return jsonb_build_object('ok', true, 'reason', 'disabled');
end;
$$;

grant execute on function public.dkd_disable_push_token(text) to authenticated;

commit;
