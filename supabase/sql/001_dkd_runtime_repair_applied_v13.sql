create or replace view public.dkd_business_daily_metrics as
select
  dkd_alias_scan.business_id,
  (dkd_alias_scan.created_at at time zone 'Europe/Istanbul')::date as bucket_day,
  count(*)::int as scan_count,
  count(distinct dkd_alias_scan.player_id)::int as unique_players,
  coalesce(dkd_alias_coupon.coupon_count, 0)::int as coupon_count,
  case
    when count(*) = 0 then 0::numeric
    else round((coalesce(dkd_alias_coupon.coupon_count, 0)::numeric / count(*)::numeric) * 100, 2)
  end as conversion_rate_pct,
  count(distinct case when dkd_alias_scan.player_id is not null then dkd_alias_scan.player_id end)::int as new_players,
  0::int as returning_players
from public.dkd_business_qr_scans dkd_alias_scan
left join (
  select
    public.dkd_business_coupon_uses.business_id,
    (public.dkd_business_coupon_uses.created_at at time zone 'Europe/Istanbul')::date as bucket_day,
    count(*)::int as coupon_count
  from public.dkd_business_coupon_uses
  group by public.dkd_business_coupon_uses.business_id,
           (public.dkd_business_coupon_uses.created_at at time zone 'Europe/Istanbul')::date
) dkd_alias_coupon
  on dkd_alias_coupon.business_id = dkd_alias_scan.business_id
 and dkd_alias_coupon.bucket_day = (dkd_alias_scan.created_at at time zone 'Europe/Istanbul')::date
group by dkd_alias_scan.business_id,
         (dkd_alias_scan.created_at at time zone 'Europe/Istanbul')::date,
         dkd_alias_coupon.coupon_count;

