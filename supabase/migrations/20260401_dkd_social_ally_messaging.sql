create or replace function public.dkd_social_touch_presence()
returns void
language plpgsql
security definer
set search_path = public
as $$
begin
  if auth.uid() is null then
    return;
  end if;

  insert into public.dkd_profiles (user_id)
  values (auth.uid())
  on conflict (user_id) do update
    set social_last_seen_at = now();

  update public.dkd_profiles
  set social_last_seen_at = now(),
      social_status = 'online'
  where user_id = auth.uid();
end;
$$;

revoke all on function public.dkd_social_touch_presence() from public;
grant execute on function public.dkd_social_touch_presence() to authenticated;

create or replace function public.dkd_social_search_profiles(dkd_param_query text, dkd_param_limit integer default 12)
returns table (
  user_id uuid,
  ally_id bigint,
  nickname text,
  avatar_emoji text,
  level integer,
  rank_key text,
  is_friend boolean,
  pending_sent boolean,
  pending_received boolean,
  request_id bigint
)
language sql
security definer
set search_path = public
stable
as $$
  with me as (
    select auth.uid() as user_id,
           regexp_replace(coalesce(trim(dkd_param_query), ''), '[^0-9]', '', 'g') as digits,
           lower(coalesce(trim(dkd_param_query), '')) as q
  )
  select
    dkd_alias_p.user_id,
    dkd_alias_p.ally_id,
    coalesce(nullif(trim(dkd_alias_p.nickname), ''), 'Oyuncu') as nickname,
    coalesce(nullif(trim(dkd_alias_p.avatar_emoji), ''), '🦅') as avatar_emoji,
    coalesce(dkd_alias_p.level, 1) as level,
    coalesce(dkd_alias_p.rank_key, 'rookie') as rank_key,
    exists (
      select 1
      from public.dkd_friendships dkd_alias_f
      where (dkd_alias_f.user_low = dkd_alias_p.user_id and dkd_alias_f.user_high = (select user_id from me))
         or (dkd_alias_f.user_high = dkd_alias_p.user_id and dkd_alias_f.user_low = (select user_id from me))
    ) as is_friend,
    exists (
      select 1
      from public.dkd_friend_requests dkd_alias_r
      where dkd_alias_r.requester_id = (select user_id from me)
        and dkd_alias_r.addressee_id = dkd_alias_p.user_id
        and dkd_alias_r.status = 'pending'
    ) as pending_sent,
    exists (
      select 1
      from public.dkd_friend_requests dkd_alias_r
      where dkd_alias_r.requester_id = dkd_alias_p.user_id
        and dkd_alias_r.addressee_id = (select user_id from me)
        and dkd_alias_r.status = 'pending'
    ) as pending_received,
    (
      select dkd_alias_r.id
      from public.dkd_friend_requests dkd_alias_r
      where dkd_alias_r.requester_id = dkd_alias_p.user_id
        and dkd_alias_r.addressee_id = (select user_id from me)
        and dkd_alias_r.status = 'pending'
      order by dkd_alias_r.created_at desc
      limit 1
    ) as request_id
  from public.dkd_profiles dkd_alias_p
  cross join me
  where me.user_id is not null
    and dkd_alias_p.user_id <> me.user_id
    and coalesce(trim(dkd_param_query), '') <> ''
    and (
      (me.digits <> '' and dkd_alias_p.ally_id::text = me.digits)
      or lower(coalesce(dkd_alias_p.nickname, '')) like ('%' || me.q || '%')
    )
  order by
    case when me.digits <> '' and dkd_alias_p.ally_id::text = me.digits then 0 else 1 end,
    case when lower(coalesce(dkd_alias_p.nickname, '')) = me.q then 0 else 1 end,
    dkd_alias_p.level desc,
    dkd_alias_p.updated_at desc
  limit greatest(1, least(coalesce(dkd_param_limit, 12), 20));
$$;

revoke all on function public.dkd_social_search_profiles(text, integer) from public;
grant execute on function public.dkd_social_search_profiles(text, integer) to authenticated;

create or replace function public.dkd_social_send_friend_request(dkd_param_target_user_id uuid)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  dkd_user_id uuid := auth.uid();
  dkd_target uuid := dkd_param_target_user_id;
  dkd_low uuid;
  dkd_high uuid;
  dkd_friendship_id bigint;
  dkd_request_id bigint;
begin
  if dkd_user_id is null then
    return jsonb_build_object('ok', false, 'reason', 'auth_required');
  end if;

  insert into public.dkd_profiles (user_id) values (dkd_user_id) on conflict (user_id) do nothing;
  insert into public.dkd_profiles (user_id) values (dkd_target) on conflict (user_id) do nothing;

  if dkd_target is null then
    return jsonb_build_object('ok', false, 'reason', 'target_not_found');
  end if;

  if dkd_target = dkd_user_id then
    return jsonb_build_object('ok', false, 'reason', 'cannot_friend_self');
  end if;

  dkd_low := case when dkd_user_id::text < dkd_target::text then dkd_user_id else dkd_target end;
  dkd_high := case when dkd_user_id::text < dkd_target::text then dkd_target else dkd_user_id end;

  select dkd_alias_f.id into dkd_friendship_id
  from public.dkd_friendships dkd_alias_f
  where dkd_alias_f.user_low = dkd_low and dkd_alias_f.user_high = dkd_high
  limit 1;

  if dkd_friendship_id is not null then
    return jsonb_build_object('ok', false, 'reason', 'friend_exists');
  end if;

  select dkd_alias_r.id into dkd_request_id
  from public.dkd_friend_requests dkd_alias_r
  where dkd_alias_r.requester_id = dkd_target
    and dkd_alias_r.addressee_id = dkd_user_id
    and dkd_alias_r.status = 'pending'
  order by dkd_alias_r.created_at desc
  limit 1;

  if dkd_request_id is not null then
    perform public.dkd_social_respond_friend_request(dkd_request_id, 'accept');
    return jsonb_build_object('ok', true, 'reason', 'accepted_existing');
  end if;

  select dkd_alias_r.id into dkd_request_id
  from public.dkd_friend_requests dkd_alias_r
  where dkd_alias_r.requester_id = dkd_user_id
    and dkd_alias_r.addressee_id = dkd_target
    and dkd_alias_r.status = 'pending'
  order by dkd_alias_r.created_at desc
  limit 1;

  if dkd_request_id is not null then
    return jsonb_build_object('ok', false, 'reason', 'request_exists');
  end if;

  insert into public.dkd_friend_requests (requester_id, addressee_id, status)
  values (dkd_user_id, dkd_target, 'pending');

  return jsonb_build_object('ok', true, 'reason', 'request_sent');
end;
$$;

revoke all on function public.dkd_social_send_friend_request(uuid) from public;
grant execute on function public.dkd_social_send_friend_request(uuid) to authenticated;

create or replace function public.dkd_social_respond_friend_request(dkd_param_request_id bigint, dkd_param_action text)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  dkd_user_id uuid := auth.uid();
  dkd_request public.dkd_friend_requests%rowtype;
  dkd_low uuid;
  dkd_high uuid;
  dkd_friendship_id bigint;
  dkd_thread_id uuid;
  dkd_action text := lower(coalesce(trim(dkd_param_action), 'accept'));
begin
  if dkd_user_id is null then
    return jsonb_build_object('ok', false, 'reason', 'auth_required');
  end if;

  select * into dkd_request
  from public.dkd_friend_requests
  where id = dkd_param_request_id
  limit 1;

  if dkd_request.id is null then
    return jsonb_build_object('ok', false, 'reason', 'request_not_found');
  end if;

  if dkd_request.addressee_id <> dkd_user_id then
    return jsonb_build_object('ok', false, 'reason', 'not_request_target');
  end if;

  if dkd_request.status <> 'pending' then
    return jsonb_build_object('ok', false, 'reason', 'request_closed');
  end if;

  if dkd_action not in ('accept', 'reject') then
    return jsonb_build_object('ok', false, 'reason', 'invalid_action');
  end if;

  update public.dkd_friend_requests
  set status = case when dkd_action = 'accept' then 'accepted' else 'rejected' end,
      responded_at = now()
  where id = dkd_request.id;

  if dkd_action = 'reject' then
    return jsonb_build_object('ok', true, 'reason', 'rejected');
  end if;

  dkd_low := case when dkd_request.requester_id::text < dkd_request.addressee_id::text then dkd_request.requester_id else dkd_request.addressee_id end;
  dkd_high := case when dkd_request.requester_id::text < dkd_request.addressee_id::text then dkd_request.addressee_id else dkd_request.requester_id end;

  insert into public.dkd_friendships (user_low, user_high, created_by)
  values (dkd_low, dkd_high, dkd_user_id)
  on conflict (user_low, user_high) do nothing;

  select id into dkd_friendship_id
  from public.dkd_friendships
  where user_low = dkd_low and user_high = dkd_high
  limit 1;

  insert into public.dkd_dm_threads (friendship_id, last_message_text, last_message_at)
  values (dkd_friendship_id, null, null)
  on conflict (friendship_id) do nothing;

  select id into dkd_thread_id
  from public.dkd_dm_threads
  where friendship_id = dkd_friendship_id
  limit 1;

  update public.dkd_friend_requests
  set status = 'canceled', responded_at = now()
  where requester_id = dkd_request.addressee_id
    and addressee_id = dkd_request.requester_id
    and status = 'pending';

  return jsonb_build_object('ok', true, 'reason', 'accepted', 'thread_id', dkd_thread_id);
end;
$$;

revoke all on function public.dkd_social_respond_friend_request(bigint, text) from public;
grant execute on function public.dkd_social_respond_friend_request(bigint, text) to authenticated;

create or replace function public.dkd_social_remove_friend(dkd_param_friend_user_id uuid)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  dkd_user_id uuid := auth.uid();
  dkd_low uuid;
  dkd_high uuid;
begin
  if dkd_user_id is null then
    return jsonb_build_object('ok', false, 'reason', 'auth_required');
  end if;

  if dkd_param_friend_user_id is null then
    return jsonb_build_object('ok', false, 'reason', 'target_not_found');
  end if;

  dkd_low := case when dkd_user_id::text < dkd_param_friend_user_id::text then dkd_user_id else dkd_param_friend_user_id end;
  dkd_high := case when dkd_user_id::text < dkd_param_friend_user_id::text then dkd_param_friend_user_id else dkd_user_id end;

  delete from public.dkd_friendships
  where user_low = dkd_low and user_high = dkd_high;

  update public.dkd_friend_requests
  set status = 'canceled', responded_at = now()
  where status = 'pending'
    and (
      (requester_id = dkd_user_id and addressee_id = dkd_param_friend_user_id)
      or (requester_id = dkd_param_friend_user_id and addressee_id = dkd_user_id)
    );

  return jsonb_build_object('ok', true, 'reason', 'removed');
end;
$$;

