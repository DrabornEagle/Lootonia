create or replace function public.dkd_social_thread_messages(dkd_param_thread_id uuid, dkd_param_limit integer default 80)
returns table (
  id bigint,
  sender_id uuid,
  body text,
  created_at timestamptz,
  seen_at timestamptz
)
language plpgsql
security definer
set search_path = public
as $$
declare
  dkd_user_id uuid := auth.uid();
  dkd_allowed boolean := false;
begin
  if dkd_user_id is null then
    raise exception 'auth_required';
  end if;

  select exists (
    select 1
    from public.dkd_dm_threads dkd_alias_t
    join public.dkd_friendships dkd_alias_f on dkd_alias_f.id = dkd_alias_t.friendship_id
    where dkd_alias_t.id = dkd_param_thread_id
      and (dkd_alias_f.user_low = dkd_user_id or dkd_alias_f.user_high = dkd_user_id)
  ) into dkd_allowed;

  if not dkd_allowed then
    raise exception 'thread_forbidden';
  end if;

  return query
  select m.id, m.sender_id, m.body, m.created_at, m.seen_at
  from public.dkd_dm_messages m
  where m.thread_id = dkd_param_thread_id
  order by m.created_at desc
  limit greatest(10, least(coalesce(dkd_param_limit, 80), 150));
end;
$$;

