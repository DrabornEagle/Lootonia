create or replace function public.dkd_social_send_message(dkd_param_thread_id uuid, dkd_param_body text)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  dkd_user_id uuid := auth.uid();
  dkd_allowed boolean := false;
  dkd_text text := trim(coalesce(dkd_param_body, ''));
begin
  if dkd_user_id is null then
    return jsonb_build_object('ok', false, 'reason', 'auth_required');
  end if;

  if dkd_text = '' then
    return jsonb_build_object('ok', false, 'reason', 'message_empty');
  end if;

  select exists (
    select 1
    from public.dkd_dm_threads dkd_alias_t
    join public.dkd_friendships dkd_alias_f on dkd_alias_f.id = dkd_alias_t.friendship_id
    where dkd_alias_t.id = dkd_param_thread_id
      and (dkd_alias_f.user_low = dkd_user_id or dkd_alias_f.user_high = dkd_user_id)
  ) into dkd_allowed;

  if not dkd_allowed then
    return jsonb_build_object('ok', false, 'reason', 'thread_forbidden');
  end if;

  insert into public.dkd_dm_messages (thread_id, sender_id, body)
  values (dkd_param_thread_id, dkd_user_id, dkd_text);

  update public.dkd_dm_threads
  set last_message_text = left(dkd_text, 120),
      last_message_at = now(),
      updated_at = now()
  where id = dkd_param_thread_id;

  update public.dkd_profiles
  set social_last_seen_at = now(),
      social_status = 'online'
  where user_id = dkd_user_id;

  return jsonb_build_object('ok', true);
end;
$$;

revoke all on function public.dkd_social_send_message(uuid, text) from public;
grant execute on function public.dkd_social_send_message(uuid, text) to authenticated;

create or replace function public.dkd_social_mark_thread_seen(dkd_param_thread_id uuid)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  dkd_user_id uuid := auth.uid();
  dkd_allowed boolean := false;
  dkd_count integer := 0;
begin
  if dkd_user_id is null then
    return jsonb_build_object('ok', false, 'reason', 'auth_required');
  end if;

  select exists (
    select 1
    from public.dkd_dm_threads dkd_alias_t
    join public.dkd_friendships dkd_alias_f on dkd_alias_f.id = dkd_alias_t.friendship_id
    where dkd_alias_t.id = dkd_param_thread_id
      and (dkd_alias_f.user_low = dkd_user_id or dkd_alias_f.user_high = dkd_user_id)
  ) into dkd_allowed;

  if not dkd_allowed then
    return jsonb_build_object('ok', false, 'reason', 'thread_forbidden');
  end if;

  update public.dkd_dm_messages
  set seen_at = now()
  where thread_id = dkd_param_thread_id
    and sender_id <> dkd_user_id
    and seen_at is null;

  get diagnostics dkd_count = row_count;

  return jsonb_build_object('ok', true, 'updated', dkd_count);
end;
$$;

