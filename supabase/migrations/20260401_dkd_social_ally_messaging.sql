  id bigint generated by default as identity primary key,
  requester_id uuid not null references public.dkd_profiles(user_id) on delete cascade,
  addressee_id uuid not null references public.dkd_profiles(user_id) on delete cascade,
  status text not null default 'pending',
  created_at timestamptz not null default now(),
  responded_at timestamptz,
  constraint dkd_friend_requests_not_self check (requester_id <> addressee_id),
  constraint dkd_friend_requests_status check (status in ('pending', 'accepted', 'rejected', 'canceled'))
);

create index if not exists idx_dkd_friend_requests_requester on public.dkd_friend_requests(requester_id, created_at desc);
create index if not exists idx_dkd_friend_requests_addressee on public.dkd_friend_requests(addressee_id, created_at desc);
create unique index if not exists uq_dkd_friend_requests_pending_pair on public.dkd_friend_requests(requester_id, addressee_id) where status = 'pending';

create table if not exists public.dkd_friendships (
  id bigint generated by default as identity primary key,
  user_low uuid not null references public.dkd_profiles(user_id) on delete cascade,
  user_high uuid not null references public.dkd_profiles(user_id) on delete cascade,
  created_by uuid references public.dkd_profiles(user_id) on delete set null,
  created_at timestamptz not null default now(),
  constraint dkd_friendships_order check (user_low <> user_high)
);

create unique index if not exists uq_dkd_friendships_pair on public.dkd_friendships(user_low, user_high);
create index if not exists idx_dkd_friendships_user_low on public.dkd_friendships(user_low, created_at desc);
create index if not exists idx_dkd_friendships_user_high on public.dkd_friendships(user_high, created_at desc);

create table if not exists public.dkd_dm_threads (
  id uuid primary key default gen_random_uuid(),
  friendship_id bigint not null unique references public.dkd_friendships(id) on delete cascade,
  last_message_text text,
  last_message_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists idx_dkd_dm_threads_last_message_at on public.dkd_dm_threads(last_message_at desc nulls last);

