  if public.dkd_text_contains_any_dkd(dkd_message_text_value, array['siparişi teslim etti', 'siparisi teslim etti', 'sipariş tamamlandı', 'siparis tamamlandi', 'teslim edildi']) then
    perform public.dkd_urgent_courier_emit_event_push_dkd(new.dkd_order_id, 'dkd_urgent_delivery_completed', new.dkd_message_id, null);
  end if;

  if public.dkd_text_contains_any_dkd(dkd_message_text_value, array['iptal etti', 'iptal edildi', 'cancelled', 'canceled']) then
    perform public.dkd_urgent_courier_emit_event_push_dkd(new.dkd_order_id, 'dkd_urgent_order_cancelled', new.dkd_message_id, null);
