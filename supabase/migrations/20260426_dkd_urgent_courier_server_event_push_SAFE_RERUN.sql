create or replace function public.dkd_urgent_courier_order_event_trigger_dkd()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  dkd_old_json_value jsonb := coalesce(to_jsonb(old), '{}'::jsonb);
  dkd_new_json_value jsonb := coalesce(to_jsonb(new), '{}'::jsonb);
  dkd_old_status_value text := lower(coalesce(dkd_old_json_value ->> 'dkd_status_key', dkd_old_json_value ->> 'status', ''));
  dkd_new_status_value text := lower(coalesce(dkd_new_json_value ->> 'dkd_status_key', dkd_new_json_value ->> 'status', ''));
  dkd_old_courier_user_id_value uuid := public.dkd_safe_uuid_from_text_dkd(dkd_old_json_value ->> 'dkd_courier_user_id');
  dkd_new_courier_user_id_value uuid := public.dkd_safe_uuid_from_text_dkd(dkd_new_json_value ->> 'dkd_courier_user_id');
  dkd_old_fee_value numeric := public.dkd_safe_numeric_from_text_dkd(coalesce(dkd_old_json_value ->> 'dkd_courier_fee_tl', dkd_old_json_value ->> 'courier_fee_tl', '0'), 0);
  dkd_new_fee_value numeric := public.dkd_safe_numeric_from_text_dkd(coalesce(dkd_new_json_value ->> 'dkd_courier_fee_tl', dkd_new_json_value ->> 'courier_fee_tl', '0'), 0);
  dkd_old_product_total_value numeric := public.dkd_safe_numeric_from_text_dkd(coalesce(dkd_old_json_value ->> 'dkd_product_total_tl', dkd_old_json_value ->> 'product_total_tl', '0'), 0);
  dkd_new_product_total_value numeric := public.dkd_safe_numeric_from_text_dkd(coalesce(dkd_new_json_value ->> 'dkd_product_total_tl', dkd_new_json_value ->> 'product_total_tl', '0'), 0);
  dkd_old_invoice_value text := coalesce(dkd_old_json_value ->> 'dkd_invoice_url', dkd_old_json_value ->> 'invoice_url', dkd_old_json_value ->> 'dkd_invoice_image_url', '');
  dkd_new_invoice_value text := coalesce(dkd_new_json_value ->> 'dkd_invoice_url', dkd_new_json_value ->> 'invoice_url', dkd_new_json_value ->> 'dkd_invoice_image_url', '');
  dkd_status_changed_value boolean := false;
begin
  if tg_op <> 'UPDATE' then
    return new;
  end if;

  dkd_status_changed_value := dkd_new_status_value is distinct from dkd_old_status_value;

  if dkd_old_courier_user_id_value is null and dkd_new_courier_user_id_value is not null then
    perform public.dkd_urgent_courier_emit_event_push_dkd(new.dkd_order_id, 'dkd_urgent_courier_accepted', null, null);
  end if;

  if dkd_new_courier_user_id_value is not null and dkd_new_fee_value > 0 and dkd_new_fee_value is distinct from dkd_old_fee_value then
    perform public.dkd_urgent_courier_emit_event_push_dkd(new.dkd_order_id, 'dkd_urgent_courier_accepted', null, null);
  end if;

  if dkd_status_changed_value and public.dkd_text_contains_any_dkd(dkd_new_status_value, array['cancel', 'iptal']) then
    perform public.dkd_urgent_courier_emit_event_push_dkd(new.dkd_order_id, 'dkd_urgent_order_cancelled', null, null);
  end if;

  if dkd_status_changed_value and public.dkd_text_contains_any_dkd(dkd_new_status_value, array['fee_approved', 'courier_shopping', 'shopping', 'tasima_onay', 'taşıma_onay', 'transport_approved']) then
    perform public.dkd_urgent_courier_emit_event_push_dkd(new.dkd_order_id, 'dkd_urgent_fee_approved', null, null);
    perform public.dkd_urgent_courier_emit_event_push_dkd(new.dkd_order_id, 'dkd_urgent_chat_opened', null, null);
  end if;

  if dkd_new_product_total_value > 0 and dkd_new_product_total_value is distinct from dkd_old_product_total_value then
    perform public.dkd_urgent_courier_emit_event_push_dkd(new.dkd_order_id, 'dkd_urgent_product_total_sent', null, null);
  end if;

  if dkd_status_changed_value and public.dkd_text_contains_any_dkd(dkd_new_status_value, array['product_total_approved', 'product_approved', 'urun_onay', 'ürün_onay']) then
    perform public.dkd_urgent_courier_emit_event_push_dkd(new.dkd_order_id, 'dkd_urgent_product_total_approved', null, null);
  end if;

  if nullif(trim(dkd_new_invoice_value), '') is not null and dkd_new_invoice_value is distinct from dkd_old_invoice_value then
    perform public.dkd_urgent_courier_emit_event_push_dkd(new.dkd_order_id, 'dkd_urgent_invoice_uploaded', null, null);
  end if;

  if dkd_status_changed_value and public.dkd_text_contains_any_dkd(dkd_new_status_value, array['invoice_uploaded', 'fatura']) then
    perform public.dkd_urgent_courier_emit_event_push_dkd(new.dkd_order_id, 'dkd_urgent_invoice_uploaded', null, null);
  end if;

  if dkd_status_changed_value and public.dkd_text_contains_any_dkd(dkd_new_status_value, array['items_picked', 'picked_up', 'on_the_way', 'delivery_started', 'yola', 'teslim_aldi', 'teslim_aldı']) then
    perform public.dkd_urgent_courier_emit_event_push_dkd(new.dkd_order_id, 'dkd_urgent_items_picked_up', null, null);
  end if;

  if dkd_status_changed_value and public.dkd_text_contains_any_dkd(dkd_new_status_value, array['delivered', 'completed', 'complete', 'done', 'finished', 'teslim_edildi', 'tamam']) then
    perform public.dkd_urgent_courier_emit_event_push_dkd(new.dkd_order_id, 'dkd_urgent_delivery_completed', null, null);
  end if;

  return new;
end;
$$;

create or replace function public.dkd_urgent_courier_message_event_trigger_dkd()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  dkd_message_json_value jsonb := coalesce(to_jsonb(new), '{}'::jsonb);
  dkd_message_text_value text := lower(coalesce(dkd_message_json_value ->> 'dkd_message_text', dkd_message_json_value ->> 'message_text', dkd_message_json_value ->> 'text', ''));
  dkd_sender_role_value text := lower(coalesce(dkd_message_json_value ->> 'dkd_sender_role_key', dkd_message_json_value ->> 'sender_role', ''));
  dkd_sender_user_id_value uuid := public.dkd_safe_uuid_from_text_dkd(dkd_message_json_value ->> 'dkd_sender_user_id');
  dkd_customer_user_id_value uuid;
  dkd_courier_user_id_value uuid;
  dkd_effective_sender_role_value text;
  dkd_is_human_message_value boolean := false;
begin
  select dkd_order_row.dkd_customer_user_id, dkd_order_row.dkd_courier_user_id
    into dkd_customer_user_id_value, dkd_courier_user_id_value
    from public.dkd_urgent_courier_orders dkd_order_row
   where dkd_order_row.dkd_order_id = new.dkd_order_id
   limit 1;

  dkd_effective_sender_role_value := dkd_sender_role_value;
  if dkd_effective_sender_role_value = '' and dkd_sender_user_id_value is not null then
    if dkd_sender_user_id_value = dkd_customer_user_id_value then
      dkd_effective_sender_role_value := 'dkd_customer';
    elsif dkd_sender_user_id_value = dkd_courier_user_id_value then
      dkd_effective_sender_role_value := 'dkd_courier';
    end if;
  end if;

  dkd_is_human_message_value := dkd_effective_sender_role_value in ('dkd_customer', 'customer', 'musteri', 'müşteri', 'dkd_courier', 'courier', 'kurye')
    and not public.dkd_text_contains_any_dkd(dkd_message_text_value, array[
