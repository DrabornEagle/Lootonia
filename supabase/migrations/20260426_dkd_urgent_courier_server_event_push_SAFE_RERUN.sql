create or replace function public.dkd_urgent_courier_emit_event_push_dkd(
  dkd_param_order_id uuid,
  dkd_param_event_key text,
  dkd_param_message_id uuid default null,
  dkd_param_dedupe_suffix text default null
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  dkd_target_row record;
  dkd_effective_dedupe_key_value text;
  dkd_queued_count_value integer := 0;
  dkd_skipped_count_value integer := 0;
  dkd_request_id_value bigint;
begin
  if dkd_param_order_id is null or nullif(trim(coalesce(dkd_param_event_key, '')), '') is null then
    return jsonb_build_object('ok', false, 'reason', 'dkd_invalid_event_payload');
  end if;

  for dkd_target_row in
    select * from public.dkd_urgent_courier_push_targets_dkd(
      dkd_param_order_id,
      dkd_param_event_key,
      dkd_param_message_id
    )
  loop
    dkd_effective_dedupe_key_value := case when nullif(trim(coalesce(dkd_param_dedupe_suffix, '')), '') is null then dkd_target_row.dkd_dedupe_key else dkd_target_row.dkd_dedupe_key || ':' || nullif(trim(dkd_param_dedupe_suffix), '') end;

    insert into public.dkd_urgent_courier_push_audit (
      dkd_dedupe_key,
      dkd_event_key,
      dkd_order_id,
      dkd_message_id,
      dkd_target_user_id,
      expo_push_token,
      dkd_title,
      dkd_body,
      dkd_payload,
      dkd_send_status
    ) values (
      dkd_effective_dedupe_key_value,
      dkd_param_event_key,
      dkd_param_order_id,
      dkd_param_message_id,
      dkd_target_row.dkd_target_user_id,
      dkd_target_row.expo_push_token,
      dkd_target_row.dkd_title,
      dkd_target_row.dkd_body,
      dkd_target_row.dkd_payload,
      'pending'
    ) on conflict (dkd_dedupe_key) do nothing;

    if not found then
      dkd_skipped_count_value := dkd_skipped_count_value + 1;
      continue;
    end if;

    begin
      select net.http_post(
        url := 'https://exp.host/--/api/v2/push/send',
        headers := jsonb_build_object(
          'Content-Type', 'application/json',
          'Accept', 'application/json',
          'Accept-Encoding', 'gzip, deflate'
        ),
        body := jsonb_build_object(
          'to', dkd_target_row.expo_push_token,
          'sound', 'default',
          'title', dkd_target_row.dkd_title,
          'body', dkd_target_row.dkd_body,
          'channelId', 'default',
          'data', dkd_target_row.dkd_payload
        ),
        timeout_milliseconds := 1500
      ) into dkd_request_id_value;

      update public.dkd_urgent_courier_push_audit
         set dkd_send_status = 'queued_pg_net',
             dkd_send_error = null,
             dkd_sent_at = timezone('utc', now())
       where dkd_dedupe_key = dkd_effective_dedupe_key_value;

      dkd_queued_count_value := dkd_queued_count_value + 1;
    exception
      when invalid_schema_name or undefined_function then
        update public.dkd_urgent_courier_push_audit
           set dkd_send_status = 'pg_net_missing',
               dkd_send_error = 'pg_net extension açık değil veya net.http_post kullanılamıyor.'
         where dkd_dedupe_key = dkd_effective_dedupe_key_value;
        dkd_skipped_count_value := dkd_skipped_count_value + 1;
      when others then
        update public.dkd_urgent_courier_push_audit
           set dkd_send_status = 'failed',
               dkd_send_error = sqlerrm
         where dkd_dedupe_key = dkd_effective_dedupe_key_value;
        dkd_skipped_count_value := dkd_skipped_count_value + 1;
    end;
  end loop;

  return jsonb_build_object(
    'ok', true,
    'dkd_event_key', dkd_param_event_key,
    'dkd_order_id', dkd_param_order_id,
    'dkd_queued_count', dkd_queued_count_value,
    'dkd_skipped_count', dkd_skipped_count_value
  );
end;
$$;

