      'fatura',
      'yola çıktı',
      'yola cikti',
      'teslim edildi',
      'tamamlandı',
      'tamamlandi'
    ]);

  if dkd_is_human_message_value then
    perform public.dkd_urgent_courier_emit_event_push_dkd(new.dkd_order_id, 'dkd_urgent_message_sent', new.dkd_message_id, null);
