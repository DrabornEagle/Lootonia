    perform public.dkd_urgent_courier_emit_event_push_dkd(new.dkd_order_id, 'dkd_urgent_courier_accepted', new.dkd_message_id, null);
  end if;

  if public.dkd_text_contains_any_dkd(dkd_message_text_value, array['taşıma ücreti onay', 'tasima ucreti onay']) then
    perform public.dkd_urgent_courier_emit_event_push_dkd(new.dkd_order_id, 'dkd_urgent_fee_approved', new.dkd_message_id, null);
  end if;

  if public.dkd_text_contains_any_dkd(dkd_message_text_value, array['sohbet aktif', 'canlı sohbet', 'canli sohbet']) then
    perform public.dkd_urgent_courier_emit_event_push_dkd(new.dkd_order_id, 'dkd_urgent_chat_opened', new.dkd_message_id, null);
  end if;

  if public.dkd_text_contains_any_dkd(dkd_message_text_value, array['mağaza bazlı', 'magaza bazli', 'ürün toplamları', 'urun toplamlari', 'ürün toplamı müşteriye', 'urun toplami musteriy']) then
    perform public.dkd_urgent_courier_emit_event_push_dkd(new.dkd_order_id, 'dkd_urgent_product_total_sent', new.dkd_message_id, null);
  end if;

  if public.dkd_text_contains_any_dkd(dkd_message_text_value, array['ürün toplamını onay', 'urun toplamini onay', 'ürün toplamı onay', 'urun toplami onay']) then
    perform public.dkd_urgent_courier_emit_event_push_dkd(new.dkd_order_id, 'dkd_urgent_product_total_approved', new.dkd_message_id, null);
  end if;

  if public.dkd_text_contains_any_dkd(dkd_message_text_value, array['fatura yük', 'fatura yuk', 'fatura eklendi']) then
    perform public.dkd_urgent_courier_emit_event_push_dkd(new.dkd_order_id, 'dkd_urgent_invoice_uploaded', new.dkd_message_id, null);
  end if;

  if public.dkd_text_contains_any_dkd(dkd_message_text_value, array['ürünleri teslim aldı', 'urunleri teslim aldi', 'yola çıktı', 'yola cikti', 'teslimat adresine doğru']) then
    perform public.dkd_urgent_courier_emit_event_push_dkd(new.dkd_order_id, 'dkd_urgent_items_picked_up', new.dkd_message_id, null);
