create or replace function public.dkd_safe_uuid_from_text_dkd(dkd_param_text text)
returns uuid
language plpgsql
immutable
as $$
declare
  dkd_clean_text_value text := nullif(trim(coalesce(dkd_param_text, '')), '');
begin
  if dkd_clean_text_value is null then
    return null;
  end if;
  return dkd_clean_text_value::uuid;
exception
  when invalid_text_representation then
    return null;
end;
$$;

create or replace function public.dkd_safe_numeric_from_text_dkd(dkd_param_text text, dkd_param_fallback numeric default 0)
returns numeric
language plpgsql
immutable
as $$
declare
  dkd_clean_text_value text := nullif(trim(coalesce(dkd_param_text, '')), '');
begin
  if dkd_clean_text_value is null then
    return coalesce(dkd_param_fallback, 0);
  end if;
  return dkd_clean_text_value::numeric;
exception
  when invalid_text_representation then
    return coalesce(dkd_param_fallback, 0);
end;
$$;

create or replace function public.dkd_text_contains_any_dkd(
  dkd_param_text text,
  dkd_param_terms text[]
)
returns boolean
language plpgsql
immutable
as $$
declare
  dkd_text_value text := lower(coalesce(dkd_param_text, ''));
  dkd_term_value text;
begin
  foreach dkd_term_value in array coalesce(dkd_param_terms, array[]::text[]) loop
    if dkd_term_value is not null and position(lower(dkd_term_value) in dkd_text_value) > 0 then
      return true;
    end if;
  end loop;
  return false;
end;
$$;

