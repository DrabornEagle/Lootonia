create or replace function public.dkd_urgent_courier_test_latest_event_push_dkd(dkd_param_event_key text)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  dkd_order_id_value uuid;
begin
  select dkd_order_row.dkd_order_id
    into dkd_order_id_value
    from public.dkd_urgent_courier_orders dkd_order_row
   order by coalesce((to_jsonb(dkd_order_row) ->> 'dkd_updated_at')::timestamptz, (to_jsonb(dkd_order_row) ->> 'created_at')::timestamptz, timezone('utc', now())) desc
   limit 1;

  if dkd_order_id_value is null then
    return jsonb_build_object('ok', false, 'reason', 'dkd_no_urgent_order_found');
  end if;

  return public.dkd_urgent_courier_emit_event_push_dkd(dkd_order_id_value, dkd_param_event_key, null, 'manual_test_' || replace(coalesce(dkd_param_event_key, 'event'), 'dkd_', ''));
exception
  when others then
    return jsonb_build_object('ok', false, 'reason', sqlerrm);
end;
$$;

