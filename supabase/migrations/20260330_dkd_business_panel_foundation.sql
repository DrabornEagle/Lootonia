create or replace function public.dkd_business_admin_upsert(
  dkd_param_id uuid,
  dkd_param_business_key text,
  dkd_param_name text,
  dkd_param_slug text,
  dkd_param_category text,
  dkd_param_address_text text,
  dkd_param_lat numeric,
  dkd_param_lng numeric,
  dkd_param_radius_m integer,
  dkd_param_is_active boolean,
  dkd_param_sponsor_enabled boolean,
  dkd_param_opens_at time,
  dkd_param_closes_at time,
  dkd_param_stock_alert_threshold integer
)
returns public.dkd_businesses
language plpgsql
security definer
set search_path = public
as $$
declare
  dkd_row public.dkd_businesses;
begin
  if not coalesce(public.dkd_is_admin(), false) then
    raise exception 'not_admin';
  end if;

  if coalesce(nullif(trim(dkd_param_business_key), ''), '') = '' then
    raise exception 'business_key_required';
  end if;

  if coalesce(nullif(trim(dkd_param_name), ''), '') = '' then
    raise exception 'name_required';
  end if;

  if coalesce(nullif(trim(dkd_param_slug), ''), '') = '' then
    raise exception 'slug_required';
  end if;

  if dkd_param_id is null then
    insert into public.dkd_businesses (
      business_key,
      name,
      slug,
      category,
      address_text,
      lat,
      lng,
      radius_m,
      is_active,
      sponsor_enabled,
      opens_at,
      closes_at,
      stock_alert_threshold,
      created_by
    )
    values (
      lower(trim(dkd_param_business_key)),
      trim(dkd_param_name),
      lower(trim(dkd_param_slug)),
      nullif(trim(dkd_param_category), ''),
      nullif(trim(dkd_param_address_text), ''),
      dkd_param_lat,
      dkd_param_lng,
      greatest(coalesce(dkd_param_radius_m, 80), 20),
      coalesce(dkd_param_is_active, true),
      coalesce(dkd_param_sponsor_enabled, false),
      dkd_param_opens_at,
      dkd_param_closes_at,
      greatest(coalesce(dkd_param_stock_alert_threshold, 10), 0),
      auth.uid()
    )
    returning * into dkd_row;
  else
    update public.dkd_businesses
    set
      business_key = lower(trim(dkd_param_business_key)),
      name = trim(dkd_param_name),
      slug = lower(trim(dkd_param_slug)),
      category = nullif(trim(dkd_param_category), ''),
      address_text = nullif(trim(dkd_param_address_text), ''),
      lat = dkd_param_lat,
      lng = dkd_param_lng,
      radius_m = greatest(coalesce(dkd_param_radius_m, 80), 20),
      is_active = coalesce(dkd_param_is_active, true),
      sponsor_enabled = coalesce(dkd_param_sponsor_enabled, false),
      opens_at = dkd_param_opens_at,
      closes_at = dkd_param_closes_at,
      stock_alert_threshold = greatest(coalesce(dkd_param_stock_alert_threshold, 10), 0),
      updated_at = now()
    where id = dkd_param_id
    returning * into dkd_row;
  end if;

  return dkd_row;
end;
$$;

