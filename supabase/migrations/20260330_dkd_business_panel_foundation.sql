  dkd_is_allowed boolean;
  dkd_today jsonb;
  dkd_hours jsonb;
  dkd_tasks jsonb;
  dkd_campaigns jsonb;
begin
  dkd_is_allowed := public.dkd_business_is_member(dkd_param_business_id) or coalesce(public.dkd_is_admin(), false);
  if not coalesce(dkd_is_allowed, false) then
    raise exception 'not_business_member';
  end if;

  select to_jsonb(dkd_row)
  into dkd_today
  from (
    select
      dkd_metric.business_id,
      dkd_metric.local_day,
      coalesce(dkd_metric.unique_today_players, 0) as today_player_count,
      coalesce(dkd_metric.qr_scan_count, 0) as today_qr_scan_count,
      coalesce(dkd_metric.coupon_use_count, 0) as today_coupon_use_count,
      coalesce(round((coalesce(dkd_metric.coupon_use_count, 0)::numeric / nullif(coalesce(dkd_metric.qr_scan_count, 0), 0)) * 100, 2), 0) as coupon_conversion_rate,
      dkd_metric.last_scan_at
    from public.dkd_business_today_metrics dkd_metric
    where dkd_metric.business_id = dkd_param_business_id
  ) as dkd_row;

  select coalesce(jsonb_agg(to_jsonb(dkd_row) order by dkd_row.local_day desc, dkd_row.local_hour asc), '[]'::jsonb)
  into dkd_hours
  from (
    select
      business_id,
      local_day,
      local_hour,
      qr_scan_count,
      unique_player_count
    from public.dkd_business_hourly_heatmap
    where business_id = dkd_param_business_id
      and local_day >= ((now() at time zone 'Europe/Istanbul')::date - greatest(coalesce(dkd_param_limit_days, 7), 1) + 1)
    order by local_day desc, local_hour asc
  ) as dkd_row;

  select coalesce(jsonb_agg(to_jsonb(dkd_row) order by dkd_row.local_day desc, dkd_row.qr_scan_count desc, dkd_row.task_key asc), '[]'::jsonb)
  into dkd_tasks
  from (
    select
      business_id,
      local_day,
      task_key,
      source_kind,
      qr_scan_count,
      unique_player_count
    from public.dkd_business_task_attribution
    where business_id = dkd_param_business_id
      and local_day >= ((now() at time zone 'Europe/Istanbul')::date - greatest(coalesce(dkd_param_limit_days, 7), 1) + 1)
    order by local_day desc, qr_scan_count desc, task_key asc
  ) as dkd_row;

  select coalesce(jsonb_agg(to_jsonb(dkd_row) order by dkd_row.is_active desc, dkd_row.starts_at desc, dkd_row.id desc), '[]'::jsonb)
  into dkd_campaigns
  from (
    select
      id,
      campaign_key,
      title,
      subtitle,
      task_key,
      source_kind,
      starts_at,
      ends_at,
      closes_at,
      stock_total,
      stock_left,
      coupon_reward_label,
      is_active,
      auto_close_on_stock_zero
    from public.dkd_business_campaigns
    where business_id = dkd_param_business_id
    order by is_active desc, starts_at desc, id desc
  ) as dkd_row;

  return jsonb_build_object(
    'today', coalesce(dkd_today, '{}'::jsonb),
    'hours', coalesce(dkd_hours, '[]'::jsonb),
    'tasks', coalesce(dkd_tasks, '[]'::jsonb),
    'campaigns', coalesce(dkd_campaigns, '[]'::jsonb)
  );
end;
$$;

revoke all on function public.dkd_business_dashboard_snapshot(uuid, integer) from public;
grant execute on function public.dkd_business_dashboard_snapshot(uuid, integer) to authenticated;

commit;
