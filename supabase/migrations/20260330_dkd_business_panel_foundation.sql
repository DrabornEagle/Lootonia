begin;

create extension if not exists pgcrypto;

create table if not exists public.dkd_businesses (
  id uuid primary key default gen_random_uuid(),
  business_key text not null unique,
  name text not null,
  slug text not null unique,
  category text,
  address_text text,
  lat numeric(10,7),
  lng numeric(10,7),
  radius_m integer not null default 80 check (radius_m >= 20),
  is_active boolean not null default true,
  sponsor_enabled boolean not null default false,
  opens_at time,
  closes_at time,
  timezone_name text not null default 'Europe/Istanbul',
  stock_alert_threshold integer not null default 10 check (stock_alert_threshold >= 0),
  created_by uuid references auth.users(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.dkd_business_memberships (
  id bigint generated by default as identity primary key,
  business_id uuid not null references public.dkd_businesses(id) on delete cascade,
  user_id uuid not null references auth.users(id) on delete cascade,
  role_key text not null default 'staff' check (role_key in ('owner', 'manager', 'staff', 'viewer')),
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  unique (business_id, user_id)
);

create table if not exists public.dkd_business_drop_links (
  id bigint generated by default as identity primary key,
  business_id uuid not null references public.dkd_businesses(id) on delete cascade,
  drop_id uuid not null references public.dkd_drops(id) on delete cascade,
  is_primary boolean not null default false,
  qr_label text,
  created_at timestamptz not null default now(),
  unique (business_id, drop_id),
  unique (drop_id)
);

create table if not exists public.dkd_business_campaigns (
  id bigint generated by default as identity primary key,
  business_id uuid not null references public.dkd_businesses(id) on delete cascade,
  campaign_key text not null unique,
  title text not null,
  subtitle text,
  task_key text,
  source_kind text not null default 'sponsor',
  starts_at timestamptz not null default now(),
  ends_at timestamptz,
  closes_at time,
  stock_total integer not null default 0 check (stock_total >= 0),
  stock_left integer not null default 0 check (stock_left >= 0),
  coupon_reward_label text,
  coupon_code_prefix text,
  is_active boolean not null default true,
  auto_close_on_stock_zero boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.dkd_business_qr_scans (
  id bigint generated by default as identity primary key,
  business_id uuid not null references public.dkd_businesses(id) on delete cascade,
  drop_id uuid references public.dkd_drops(id) on delete set null,
  user_id uuid references auth.users(id) on delete set null,
  campaign_id bigint references public.dkd_business_campaigns(id) on delete set null,
  task_key text,
  source_kind text not null default 'drop',
  session_key text,
  scanned_at timestamptz not null default now(),
  local_day date generated always as ((scanned_at at time zone 'Europe/Istanbul')::date) stored,
  local_hour smallint generated always as (extract(hour from (scanned_at at time zone 'Europe/Istanbul'))::smallint) stored
);

create table if not exists public.dkd_business_coupon_uses (
  id bigint generated by default as identity primary key,
  business_id uuid not null references public.dkd_businesses(id) on delete cascade,
  campaign_id bigint references public.dkd_business_campaigns(id) on delete set null,
  user_id uuid references auth.users(id) on delete set null,
  coupon_code text,
  task_key text,
  status_key text not null default 'used' check (status_key in ('used', 'cancelled', 'reversed')),
  used_at timestamptz not null default now(),
  local_day date generated always as ((used_at at time zone 'Europe/Istanbul')::date) stored,
  local_hour smallint generated always as (extract(hour from (used_at at time zone 'Europe/Istanbul'))::smallint) stored
);

create index if not exists idx_dkd_business_memberships_business_user on public.dkd_business_memberships(business_id, user_id) where is_active = true;
create index if not exists idx_dkd_business_qr_scans_business_day on public.dkd_business_qr_scans(business_id, local_day desc);
create index if not exists idx_dkd_business_qr_scans_business_hour on public.dkd_business_qr_scans(business_id, local_day desc, local_hour asc);
create index if not exists idx_dkd_business_qr_scans_business_task on public.dkd_business_qr_scans(business_id, task_key, local_day desc);
create index if not exists idx_dkd_business_coupon_uses_business_day on public.dkd_business_coupon_uses(business_id, local_day desc);
create index if not exists idx_dkd_business_campaigns_business_active on public.dkd_business_campaigns(business_id, is_active, starts_at desc);

