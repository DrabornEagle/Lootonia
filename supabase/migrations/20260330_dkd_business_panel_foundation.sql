create or replace view public.dkd_business_today_metrics as
with dkd_business_day as (
  select
    dkd_business.id as business_id,
    (now() at time zone coalesce(dkd_business.timezone_name, 'Europe/Istanbul'))::date as local_day
  from public.dkd_businesses dkd_business
),
dkd_scan_agg as (
  select
    dkd_scan.business_id,
    dkd_scan.local_day,
    count(*) as qr_scan_count,
    count(distinct dkd_scan.user_id) as unique_today_players,
    max(dkd_scan.scanned_at) as last_scan_at
  from public.dkd_business_qr_scans dkd_scan
  group by dkd_scan.business_id, dkd_scan.local_day
),
dkd_coupon_agg as (
  select
    dkd_coupon.business_id,
    dkd_coupon.local_day,
    count(*) as coupon_use_count
  from public.dkd_business_coupon_uses dkd_coupon
  group by dkd_coupon.business_id, dkd_coupon.local_day
)
select
  dkd_day.business_id,
  dkd_day.local_day,
  coalesce(dkd_scan_agg.qr_scan_count, 0) as qr_scan_count,
  coalesce(dkd_scan_agg.unique_today_players, 0) as unique_player_count,
  coalesce(dkd_scan_agg.unique_today_players, 0) as unique_today_players,
  coalesce(dkd_coupon_agg.coupon_use_count, 0) as coupon_use_count,
  dkd_scan_agg.last_scan_at
from dkd_business_day dkd_day
left join dkd_scan_agg
  on dkd_scan_agg.business_id = dkd_day.business_id
 and dkd_scan_agg.local_day = dkd_day.local_day
left join dkd_coupon_agg
  on dkd_coupon_agg.business_id = dkd_day.business_id
 and dkd_coupon_agg.local_day = dkd_day.local_day;

create or replace view public.dkd_business_hourly_heatmap as
select
  dkd_scan.business_id,
  dkd_scan.local_day,
  dkd_scan.local_hour,
  count(*) as qr_scan_count,
  count(distinct dkd_scan.user_id) as unique_player_count
from public.dkd_business_qr_scans dkd_scan
group by dkd_scan.business_id, dkd_scan.local_day, dkd_scan.local_hour;

create or replace view public.dkd_business_task_attribution as
