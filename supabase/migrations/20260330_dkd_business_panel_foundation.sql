  count(distinct dkd_scan.user_id) as unique_player_count
from public.dkd_business_qr_scans dkd_scan
group by
  dkd_scan.business_id,
  dkd_scan.local_day,
  coalesce(nullif(dkd_scan.task_key, ''), 'organic'),
  coalesce(nullif(dkd_scan.source_kind, ''), 'drop');

